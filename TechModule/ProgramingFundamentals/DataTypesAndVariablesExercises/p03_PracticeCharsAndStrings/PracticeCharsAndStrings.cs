using System;

namespace p03_PracticeCharsAndStrings
{
    class PracticeCharsAndStrings
    {
        static void Main(string[] args)
        {
            string softUni = "Software University";
            char b = 'B';
            char y = 'y';
            char e = 'e';
            string lovePrograming = "I love programming";

            Console.WriteLine(
                $"{softUni}\r\n" +
                $"{b}\r\n" +
                $"{y}\r\n" +
                $"{e}\r\n" +
                $"{lovePrograming}");
        }
    }
}
